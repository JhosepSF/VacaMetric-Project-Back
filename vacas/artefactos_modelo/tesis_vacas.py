    print(f"   p-valor = {p_val:.6f} > 0.05 (no se rechaza H₀)")
    print("   Los modelos son estadísticamente equivalentes en validation.")
    print("   Se reportan diferencias descriptivas (sin inferencia estadística):\n")

    # Calcular diferencias descriptivas entre todos los pares
    models_list = list(desc_df["Modelo"])
