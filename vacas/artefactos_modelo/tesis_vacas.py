class BackboneTIMM(nn.Module):
    def __init__(self, model_name: str = BACKBONE_NAME, trainable: bool = False):
        super().__init__()
        # Intento directo; si no existe en esta versión de timm, probar con prefijo 'tv_'
        try:
            m = timm.create_model(model_name, pretrained=True, num_classes=0)
        except Exception:
            m = timm.create_model(f"tv_{model_name}", pretrained=True, num_classes=0)
        self.model = m
        # Para ResNet (timm) esto es 2048
        self.out_dim = getattr(m, 'num_features', 2048)
        if not trainable:
            for p in self.model.parameters():
                p.requires_grad = False

    def forward(self, x):
        return self.model(x)

@torch.no_grad()
def extract_embeddings_pair(backbone: BackboneTIMM, df_subset: pd.DataFrame, batch=64, workers=0):
    dl = make_loader(df_subset, batch=batch, shuffle=False, workers=workers)
    Zc, Zs, ys, morphs = [], [], [], []
    bb = backbone.to(DEVICE).eval()
    for x_cont, x_sil, y, morph in dl:
        x_cont = x_cont.to(DEVICE); x_sil = x_sil.to(DEVICE)
        zc = bb(x_cont).detach().cpu().numpy().astype(np.float32)
        zs = bb(x_sil).detach().cpu().numpy().astype(np.float32)
        Zc.append(zc); Zs.append(zs)
        ys.extend(y.numpy().tolist())
        morphs.append(morph.numpy())
    Zc = np.concatenate(Zc, axis=0) if Zc else np.zeros((0, bb.out_dim), np.float32)
    Zs = np.concatenate(Zs, axis=0) if Zs else np.zeros((0, bb.out_dim), np.float32)
    y  = np.array(ys, dtype=np.float32)
    M  = np.concatenate(morphs, axis=0).astype(np.float32) if morphs else np.zeros((0,9), np.float32)

    # imputación simple (NaN -> mean)
    if M.size:
        col_means = np.nanmean(np.where(np.isnan(M), np.nan, M), axis=0)
        inds = np.where(np.isnan(M))
        M[inds] = np.take(col_means, inds[1])

    X = np.hstack([Zc, Zs, M]) if M.size else np.hstack([Zc, Zs])
    return X, y
