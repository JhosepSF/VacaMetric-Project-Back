class PairDataset(Dataset):
    def __init__(self, df: pd.DataFrame, transform):
        self.df = df.reset_index(drop=True).copy()
        self.t = transform
        self.morph_cols = ['area_px','perim_px','bbox_w','bbox_h','aspect','hu1','hu2','hu3','fill_frac']

    def __len__(self): return len(self.df)

    def _load_rgb_safe(self, path: str) -> Image.Image:
        # 1) PIL
        try:
            with Image.open(path) as im:
                return im.convert('RGB')
        except Exception:
            pass
        # 2) OpenCV fallback
        try:
            bgr = cv2.imread(str(path))
            if bgr is not None:
                rgb = cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)
                return Image.fromarray(rgb)
        except Exception:
            pass
        # 3) Placeholder negro (para no romper el batch)
        return Image.fromarray(np.zeros((IMG_SIZE, IMG_SIZE, 3), dtype=np.uint8))

    def __getitem__(self, i):
        r = self.df.loc[i]
        y = float(r['weight'])
        cont = self._load_rgb_safe(r['contorno_path'])
        x_cont = self.t(cont)
        if r['sil_path']:
            sil = self._load_rgb_safe(r['sil_path'])
            x_sil = self.t(sil)
        else:
            x_sil = x_cont

        morph = np.array([r[c] if pd.notna(r[c]) else np.nan for c in self.morph_cols],
                         dtype=np.float32)
        return x_cont, x_sil, y, morph

def make_loader(df, batch=64, shuffle=False, workers=0):
    ds = PairDataset(df, val_tf)
    return DataLoader(
        ds, batch_size=batch, shuffle=shuffle,
        num_workers=int(workers), pin_memory=(DEVICE.type=='cuda'),
        persistent_workers=False
    )

def filter_missing(df: pd.DataFrame, verbose: bool = True) -> pd.DataFrame:
    df = df.copy()

    # Asegurar columnas y tipos
    if 'sil_path' not in df.columns:
        df['sil_path'] = ''
    df['contorno_path'] = df['contorno_path'].astype(str)
    df['sil_path']      = df['sil_path'].fillna('').astype(str)

    # Contornos: deben existir; si no, se eliminan
    okc = df['contorno_path'].apply(lambda p: Path(p).exists())
    if verbose:
        badc = df.loc[~okc, 'contorno_path']
        if len(badc):
            print(f"⚠️ {len(badc)} contornos inexistentes → se omiten. Ej.:", badc.head(3).to_list())
    df = df.loc[okc].reset_index(drop=True)

    # Siluetas: si no existen, se dejan como cadena vacía (fallback)
    oks = df['sil_path'].apply(lambda p: (p == '') or Path(p).exists())
    if verbose:
        bads = df.loc[~oks, 'sil_path']
        if len(bads):
            print(f"⚠️ {len(bads)} siluetas inexistentes → se reemplazan por ''. Ej.:", bads.head(3).to_list())
    df.loc[~oks, 'sil_path'] = ''

    return df

