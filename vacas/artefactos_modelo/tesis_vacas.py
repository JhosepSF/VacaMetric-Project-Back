def eta2_from_aov(aov_tbl, aov_orig):
    try:
        ss_model = float(aov_tbl.loc["C(model)","Suma de cuadrados"])
    except KeyError:
        ss_model = float(aov_orig.loc["C(model)","sum_sq"])
    try:
        ss_res = float(aov_orig.loc["Residual","sum_sq"])
    except KeyError:
        ss_res = float(aov_tbl.loc["Residual","Suma de cuadrados"])
    ss_tot = ss_model + ss_res
    eta2 = ss_model/ss_tot if ss_tot>0 else np.nan
    if not np.isfinite(eta2): lab="n/a"
    elif eta2 < 0.01: lab="muy pequeño"
    elif eta2 < 0.06: lab="pequeño"
    elif eta2 < 0.14: lab="mediano"
    else: lab="grande"
    return eta2, lab

