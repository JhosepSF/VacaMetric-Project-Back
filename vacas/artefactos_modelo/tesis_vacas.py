class BackboneTIMM(nn.Module):
    def __init__(self, model_name='efficientnet_b0', trainable=False):
        super().__init__()
        m = timm.create_model(model_name, pretrained=True, num_classes=0)
        self.model = m
        self.out_dim = m.num_features
        if not trainable:
            for p in self.model.parameters(): p.requires_grad = False
    def forward(self, x): return self.model(x)

@torch.no_grad()
def extract_embeddings_pair(backbone: BackboneTIMM, df_subset: pd.DataFrame, batch=64, workers=0):
    dl = make_loader(df_subset, batch=batch, shuffle=False, workers=workers)
    Zc, Zs, ys, morphs = [], [], [], []
    bb = backbone.to(DEVICE).eval()
    for x_cont, x_sil, y, morph in dl:
        x_cont = x_cont.to(DEVICE); x_sil = x_sil.to(DEVICE)
        zc = bb(x_cont).detach().cpu().numpy().astype(np.float32)
        zs = bb(x_sil).detach().cpu().numpy().astype(np.float32)
        Zc.append(zc); Zs.append(zs)
        ys.extend(y.numpy().tolist())
        morphs.append(morph.numpy())
    Zc = np.concatenate(Zc, axis=0) if Zc else np.zeros((0, bb.out_dim), np.float32)
    Zs = np.concatenate(Zs, axis=0) if Zs else np.zeros((0, bb.out_dim), np.float32)
    y  = np.array(ys, dtype=np.float32)
    M  = np.concatenate(morphs, axis=0).astype(np.float32) if morphs else np.zeros((0,9), np.float32)

    # imputación simple (NaN -> mean por columna)
    if M.size:
        col_means = np.nanmean(np.where(np.isnan(M), np.nan, M), axis=0)
        inds = np.where(np.isnan(M))
        M[inds] = np.take(col_means, inds[1])

    X = np.hstack([Zc, Zs, M]) if M.size else np.hstack([Zc, Zs])
    return X, y

def filter_missing(df):
    okc = df['contorno_path'].apply(lambda p: Path(p).exists())
    oks = df['sil_path'].apply(lambda p: (p == '') or Path(p).exists())
    badc = df.loc[~okc, 'contorno_path']
    bads = df.loc[~oks, 'sil_path']
    if len(badc): print(f"⚠️ {len(badc)} contornos inexistentes -> se omiten (ej):", badc.head(3).to_list())
    if len(bads): print(f"⚠️ {len(bads)} siluetas inexistentes -> se ponen vacías (ej):", bads.head(3).to_list())
    df2 = df.loc[okc].copy().reset_index(drop=True)
    df2.loc[~df2['sil_path'].apply(lambda p: p == '' or Path(p).exists()), 'sil_path'] = ''
    return df2

def diagnostico_embeddings(X, fold_name="", muestra_samples=5):
    """
    Diagnóstico completo de embeddings con morfológicas.

    Args:
        X: Array (N, features) con [contorno | silueta | morfológicas]
        fold_name: Nombre descriptivo (ej: "Fold 1", "HPO Global")
        muestra_samples: Cantidad de samples a mostrar
    """
    print(f"\n{'='*80}")
    print(f"🔬 DIAGNÓSTICO: {fold_name}")
    print(f"{'='*80}")

    # Dimensión total
    print(f"📊 Shape: {X.shape} (samples x features)")

    # Verificar NaN
    nan_count = np.isnan(X).sum()
    if nan_count > 0:
        print(f"⚠️ WARNING: {nan_count} NaN detectados en embeddings!")
    else:
        print(f"✅ Sin NaN")

    # Verificar infinitos
    inf_count = np.isinf(X).sum()
    if inf_count > 0:
        print(f"⚠️ WARNING: {inf_count} valores infinitos detectados!")

    # Morfológicas (últimas 9 columnas)
    if X.shape[1] >= 9:
        morph_slice = X[:, -9:]
        morph_cols = ['area_px', 'perim_px', 'bbox_w', 'bbox_h',
                      'aspect', 'hu1', 'hu2', 'hu3', 'fill_frac']

        print(f"\n📋 ESTADÍSTICAS MORFOLÓGICAS (últimas 9 columnas):")
        print(f"{'Feature':<12} {'Media':<12} {'Std':<12} {'Min':<12} {'Max':<12}")
        print("-" * 60)

        for i, col in enumerate(morph_cols):
            col_data = morph_slice[:, i]
            media = np.nanmean(col_data)
            std = np.nanstd(col_data)
            minimo = np.nanmin(col_data)
            maximo = np.nanmax(col_data)
            print(f"{col:<12} {media:<12.3f} {std:<12.3f} {minimo:<12.3f} {maximo:<12.3f}")

        # Muestras individuales
        if muestra_samples > 0:
            print(f"\n📋 SAMPLES INDIVIDUALES (primeros {muestra_samples}):")
            for idx in range(min(muestra_samples, X.shape[0])):
                print(f"   Sample {idx}:")
                for i, col in enumerate(morph_cols):
                    print(f"      {col}: {morph_slice[idx, i]:.3f}")

        # Detectar ceros anómalos
        zeros_per_col = (morph_slice == 0).sum(axis=0)
        if zeros_per_col.sum() > 0:
            print(f"\n⚠️ CEROS DETECTADOS:")
            for i, col in enumerate(morph_cols):
                if zeros_per_col[i] > 0:
                    print(f"   {col}: {zeros_per_col[i]} ceros ({100*zeros_per_col[i]/X.shape[0]:.1f}%)")
    else:
        print(f"⚠️ WARNING: Solo {X.shape[1]} columnas (esperadas >= 9 para morfológicas)")

    print(f"{'='*80}\n")

