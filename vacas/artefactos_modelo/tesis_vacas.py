def hpo_xgb_regression(X, y, groups, inner_splits=3, n_trials=40, seed=SEED,
                       study_name='xgb_hpo_global', storage_path=str(OPTUNA_DB)):
    storage_uri = f"sqlite:///{storage_path}"
    pruner = optuna.pruners.MedianPruner(n_warmup_steps=max(1, inner_splits//2))
    study = optuna.create_study(direction='minimize', study_name=study_name,
                                storage=storage_uri, load_if_exists=True, pruner=pruner)

    def objective(trial):
        params = dict(
            n_estimators      = trial.suggest_int('n_estimators', 200, 800),              # 🔧 Reducido
            max_depth         = trial.suggest_int('max_depth', 3, 6),                     # 🔧 Más restrictivo
            learning_rate     = trial.suggest_float('learning_rate', 0.01, 0.1, log=True),# 🔧 Rango más conservador
            subsample         = trial.suggest_float('subsample', 0.6, 0.85),              # 🔧 Más submuestreo
            colsample_bytree  = trial.suggest_float('colsample_bytree', 0.6, 0.85),       # 🔧 Igual
            reg_alpha         = trial.suggest_float('reg_alpha', 0.001, 1.0, log=True),   # 🔧 MUCHO MÁS ALTO
            reg_lambda        = trial.suggest_float('reg_lambda', 0.1, 10.0, log=True),   # 🔧 Más restrictivo
            min_child_weight  = trial.suggest_float('min_child_weight', 3.0, 15.0),       # 🔧 MÁS ALTO (crítico)
            gamma             = trial.suggest_float('gamma', 0.0, 5.0)                     # ✅ OK
        )
        tree_method = 'gpu_hist' if (DEVICE.type == 'cuda') else 'hist'
        inner = GroupKFold(n_splits=inner_splits)
        rmses = []
        for tr, va in inner.split(X, y, groups=groups):
            model = XGBRegressor(
                **params,
                random_state=seed,
                objective='reg:squarederror',
                tree_method=tree_method,
                n_jobs=0
            )
            model.fit(X[tr], y[tr], eval_set=[(X[va], y[va])], verbose=False)
            pred = model.predict(X[va])
            rmse = RMSE(y[va], pred)
            rmses.append(rmse)
            trial.report(float(np.mean(rmses)), len(rmses))
            if trial.should_prune():
                raise optuna.exceptions.TrialPruned()
        return float(np.mean(rmses))

    study.optimize(objective, n_trials=n_trials, show_progress_bar=False)
    return study.best_params, study.best_value

