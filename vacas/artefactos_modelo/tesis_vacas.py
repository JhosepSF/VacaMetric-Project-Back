def rotate_if_vertical(bgr):
    """Gira 90° CCW si la imagen está vertical."""
    return cv2.rotate(bgr, cv2.ROTATE_90_COUNTERCLOCKWISE) if bgr.shape[0] > bgr.shape[1] else bgr

def resize_pad_fixed(img_bgr, target_wh):
    """
    Letterbox a tamaño fijo target_wh=(W,H) conservando aspecto.
    Retorna: img_lb, scale, dx, dy  (para mapear coords si hiciera falta)
    """
    H, W = img_bgr.shape[:2]
    tw, th = target_wh
    r = min(tw / W, th / H)
    nw, nh = int(round(W * r)), int(round(H * r))
    img_rs = cv2.resize(img_bgr, (nw, nh), interpolation=cv2.INTER_AREA if r < 1 else cv2.INTER_CUBIC)
    canvas = np.full((th, tw, 3), 255, dtype=np.uint8)  # fondo blanco
    dx = (tw - nw) // 2
    dy = (th - nh) // 2
    canvas[dy:dy+nh, dx:dx+nw] = img_rs
    return canvas, r, dx, dy

def save_path_like_root(src_path: Path, src_root: Path, base_out: Path, suffix: str = '') -> Path:
    """Mantiene subcarpetas de src_root en base_out."""
    try:
        rel = src_path.relative_to(src_root)
    except ValueError:
        rel = Path(src_path.name)
    name = f"{rel.stem}{suffix}{src_path.suffix}"
    return base_out / rel.parent / name

