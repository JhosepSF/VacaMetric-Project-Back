def parse_contorno_stem(stem: str):
    """
    Acepta:
      1059-269_contorno
      1059-269_contorno_rotm30
    Retorna (cow_id, weight) o None.
    """
    m = re.search(r'^(?P<cow>.+)-(?P<w>\d+(?:\.\d+)?)_contorno(?:_.*)?$', stem, flags=re.IGNORECASE)
    if not m: return None
    return m.group('cow'), float(m.group('w'))

def build_silhouette_path(cow_id: str, weight: float) -> Optional[Path]:
    """
    Busca <id>-<peso>_sil en DATASET_DIR_SIL con variaciones de representación del peso.
    """
    wtxts = []
    if float(weight).is_integer():
        wtxts.append(str(int(weight)))
    wtxts.append(str(weight))
    wtxts.append(f"{weight:g}")  # compacto

    # Intento directo
    for w in dict.fromkeys(wtxts).keys():
        for ext in ('.jpg','.jpeg','.png','.JPG','.JPEG','.PNG'):
            p = DATASET_DIR_SIL / f"{cow_id}-{w}_sil{ext}"
            if p.exists(): return p

    # Glob recursivo
