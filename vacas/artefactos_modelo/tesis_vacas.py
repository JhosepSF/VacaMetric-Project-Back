def save_circle_meta(src_path: Path, src_root: Path, base_out: Path, circ, shape_hw):
    """Guarda circ=(cx,cy,r) y shape=(H,W) en JSON (junto a la imagen normalizada)."""
    if circ is None:
        return
    H, W = shape_hw
    cx, cy, rr = map(float, circ)
    data = {"cx": cx, "cy": cy, "r": rr, "H": int(H), "W": int(W)}
    jpath = circle_meta_path(src_path, src_root, base_out)
    jpath.parent.mkdir(parents=True, exist_ok=True)
    with open(jpath, "w") as f:
        json.dump(data, f)

def load_circle_meta(norm_img_path: Path):
    """Carga circ del JSON junto a la imagen normalizada (si existe)."""
    jpath = norm_img_path.with_suffix(".json")
    if not jpath.exists():
        return None
    try:
        with open(jpath, "r") as f:
            d = json.load(f)
        return (int(round(d["cx"])), int(round(d["cy"])), int(round(d["r"])))
    except Exception:
        return None

def get_cow_class_id(names):
    """Devuelve el ID de clase 'cow' si existe; sino None."""
    if isinstance(names, dict):
        for k, v in names.items():
            if str(v).lower() == COW_NAME:
                return int(k)
    elif isinstance(names, (list, tuple)):
        for i, v in enumerate(names):
            if str(v).lower() == COW_NAME:
                return int(i)
    return None

# --------- Detección de círculo EN EL CANVAS FIJO ---------
def detect_circle_in_canvas(bgr_fixed):
    """
    Detecta (cx,cy,r) del sticker en la imagen ya letterboxeada a FIXED_CANVAS_WH.
    Usa YOLO (si está) y cae a Hough. Trabajamos SIEMPRE en este canvas.
    """
    H, W = bgr_fixed.shape[:2]
    circ = None

    # 1) YOLO de círculo (si existe)
    if USE_YOLO_CIRCLE and circle_model is not None:
        r = circle_model.predict(bgr_fixed[:, :, ::-1], imgsz=min(max(W,H), 960), conf=0.20,
                                 device=device, verbose=False, retina_masks=True)[0]
        try:
            if r.masks is not None and len(r.masks.data) > 0:
                pts = r.masks.xy[0]  # Nx2
                pts = np.asarray(pts, dtype=np.float32)
                (cx, cy), rr = cv2.minEnclosingCircle(pts.astype(np.float32))
                circ = (float(cx), float(cy), float(rr))
            elif r.boxes is not None and len(r.boxes) > 0:
                b = r.boxes[0]
                x1, y1, x2, y2 = b.xyxy[0].cpu().numpy().tolist()
                cx = 0.5 * (x1 + x2)
                cy = 0.5 * (y1 + y2)
                rr = 0.5 * max((x2 - x1), (y2 - y1))
                circ = (float(cx), float(cy), float(rr))
        except Exception:
            circ = None

    # 2) Fallback Hough (rango razonable en este canvas)
    if circ is None:
        lab = cv2.cvtColor(bgr_fixed, cv2.COLOR_BGR2LAB)
        l = lab[:, :, 0]
        clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
        l = clahe.apply(l)
        edges = cv2.Canny(l, 50, 150)
        h, w = edges.shape[:2]
        # El sticker suele ocupar ~1%–7% del min(h,w) en radio → ajusta si hace falta
        minR = max(6, int(0.01 * min(h, w)))
        maxR = int(0.07 * min(h, w))
        circles = cv2.HoughCircles(
            edges, cv2.HOUGH_GRADIENT, dp=1.2, minDist=min(h, w)//6,
            param1=120, param2=20, minRadius=minR, maxRadius=maxR
        )
        if circles is not None:
            cand = np.uint16(np.around(circles[0]))
            cx0, cy0 = w/2.0, h/2.0
            best = None; best_score = -1e18
            for x, y, r in cand:
                ring = np.zeros_like(edges)
                cv2.circle(ring, (x, y), r, 255, 2)
                density = float((ring & (edges > 0)).sum())
                center_bonus = -((x - cx0)**2 + (y - cy0)**2)**0.5
                score = density + 0.002 * center_bonus
                if score > best_score:
                    best_score = score
                    best = (int(x), int(y), int(r))
            if best is not None:
                circ = (float(best[0]), float(best[1]), float(best[2]))

    if circ is None:
        return None
    cx, cy, rr = circ
    return (int(round(cx)), int(round(cy)), int(round(rr)))

# --------- Postpro de máscaras (igual que antes, con un componente) ---------
def keep_largest_component(bin_mask):
    m = (bin_mask > 0).astype(np.uint8)
    num, labels = cv2.connectedComponents(m)
    if num <= 1:
        return m.astype(np.uint8) * 255
    best_label, best_area = 0, -1
    for lb in range(1, num):
        area = int((labels == lb).sum())
        if area > best_area:
            best_area, best_label = area, lb
    out = (labels == best_label).astype(np.uint8) * 255
    return out

def postprocess_mask(mask, H, W):
    m = (mask > 0).astype(np.uint8) * 255
    k = max(3, int(0.008 * min(H, W)))
    K = np.ones((k, k), np.uint8)
    m = cv2.morphologyEx(m, cv2.MORPH_CLOSE, K, iterations=2)
    m = cv2.morphologyEx(m, cv2.MORPH_OPEN,  K, iterations=1)
    mm = m.copy()
    ffmask = np.zeros((H+2, W+2), np.uint8)
    cv2.floodFill(mm, ffmask, (0, 0), 255)
    holes = cv2.bitwise_not(mm)
    m = cv2.bitwise_or(m, holes)
    m = keep_largest_component(m)
    cnts, _ = cv2.findContours(m, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    if cnts:
        big = max(cnts, key=cv2.contourArea)
        eps = 0.003 * cv2.arcLength(big, True)
        smooth = cv2.approxPolyDP(big, eps, True)
        m[:] = 0
        cv2.drawContours(m, [smooth], -1, 255, thickness=cv2.FILLED)
    return m

# --------- Selección de máscara "de la vaca del sticker" ---------
def pick_best_mask(
    r, H, W, cc=None,
    min_area_frac=0.008,
    min_overlap_frac=0.25,        # exigir solape con disco del sticker
    require_center_inside=True,
    center_bonus_weight=0.15
):
    if r is None or r.masks is None or len(r.masks.data) == 0:
        return None

    raw = r.masks.data.cpu().numpy().astype(np.uint8)
    masks = [cv2.resize(m, (W, H), interpolation=cv2.INTER_NEAREST) for m in raw]
    confs = []
    if getattr(r, "boxes", None) is not None and len(r.boxes) == len(masks):
        confs = r.boxes.conf.cpu().numpy().tolist()
    else:
        confs = [0.0] * len(masks)

    area_min = int(min_area_frac * H * W)
    keep = [(i, m) for i, m in enumerate(masks) if (m > 0).sum() >= area_min]
    if not keep:
        return None
    idxs, masks = zip(*keep)
    confs = [confs[i] for i in idxs]

    if cc is not None:
        cx, cy, rr = cc
        disk = np.zeros((H, W), np.uint8)
        rcheck = max(6, int(rr * 0.6))
        cv2.circle(disk, (cx, cy), rcheck, 255, thickness=cv2.FILLED)
        disk_area = float((disk > 0).sum())

        candidates = []
        for i, m in enumerate(masks):
            inter = float(((m > 0) & (disk > 0)).sum())
            overlap = inter / max(1.0, disk_area)
            area = float((m > 0).sum())
            conf = float(confs[i])
            if require_center_inside:
                dil = cv2.dilate((m > 0).astype(np.uint8), np.ones((5,5), np.uint8), iterations=1)
                inside = (cy >= 0 and cy < H and cx >= 0 and cx < W and dil[cy, cx] > 0)
            else:
                inside = True
            candidates.append((i, overlap, area, conf, inside))

        valid = [(i, ov, ar, cf) for (i, ov, ar, cf, inside) in candidates
                 if inside and ov >= min_overlap_frac]
        if valid:
            i = max(valid, key=lambda t: (t[1], t[2], t[3]))[0]
            return (masks[i].astype(np.uint8) * 255)

        if candidates:
            i = max(candidates, key=lambda t: (t[1], t[2], t[3]))[0]
            return (masks[i].astype(np.uint8) * 255)

    best_i, best_score = None, -1e18
    for i, m in enumerate(masks):
        area = float((m > 0).sum())
        ys, xs = np.where(m > 0)
        if len(xs) == 0:
            continue
        cxm, cym = xs.mean(), ys.mean()
        center_bonus = -((cxm/W - 0.5)**2 + (cym/H - 0.5)**2)
        score = area + center_bonus_weight * area * center_bonus + 2000.0 * confs[i]
        if score > best_score:
            best_score, best_i = score, i
    return (masks[best_i].astype(np.uint8) * 255) if best_i is not None else None

