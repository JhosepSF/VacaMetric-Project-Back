def normalize_by_circle(bgr):
    """
    1) Rotar si hace falta.
    2) Letterbox a FIXED_CANVAS_WH (TODAS quedan del mismo tamaño).
    3) Detectar círculo en ese canvas y devolver coords en el canvas.
    """
